void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	temp;

	if (!tab || size < 0)
		return ;
	i = -1;
	while (++i < size / 2)
	{
		temp = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = temp;
	}
}
