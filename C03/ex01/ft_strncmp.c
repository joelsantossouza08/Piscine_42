int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (!s1 || !s2 || !n)
		return (0);
	while (n-- > 1 && *s1 == *s2)
	{
		if (!*s1)
			return (0);
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
